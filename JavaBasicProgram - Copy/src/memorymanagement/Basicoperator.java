package memorymanagement;

public class Basicoperator {

	public static void main(String[] args) {
		int num1=50,num2=40;
		System.out.println("num1: "+num1  + "num2: "+num2);//50,40
		System.out.println("(num1-num2): "+(num1-num2));//10
		System.out.println("num1+num2: "+(num1+num2));//90
		System.out.println("num1*num2: "+(num1*num2));//2000
		System.out.println("num1/num2: "+(num1/num2));//1
		System.out.println("num1%num2: "+(num1%num2));
	}

}
